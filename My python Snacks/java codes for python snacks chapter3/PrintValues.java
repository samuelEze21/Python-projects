public class PrintValues {
    public static void main(String[] args) {
        int a = 7;
        int b = 7;

        String table = b + "\n";

        // Print statements
        System.out.println(7);        // This prints the integer 7
        System.out.println(a);        // This prints the value of variable 'a' which is 7
        System.out.print(table);      // This prints 'b' as a string followed by a newline character
        System.out.println(b);        // This prints the value of variable 'b' which is 7
    }
}
